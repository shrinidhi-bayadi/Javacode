package module1;
// how to use if block conditional statement in program

public class IfBlock
{
    public static void main(String[] args) 
  {
	int age=34;
	
	if(age>=18)  // if condition is true ,this block will execute. if false, nothing execute
	{
		System.out.println("Person can vote");
	}
	
	System.out.println("Control came to the next line");
  }
}
