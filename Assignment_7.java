package module1;

//write a program to check if the number is greater than 100,less than 100 or equal to 100

public class Assignment_7 {
	public static void main(String[] args) {
		int number = 200;

		System.out.println("Enter a number : " + number);

		if (number > 100) {
			System.out.println("Number is greater than 100");

		} 
		if (number < 100) {
			System.out.println("Number is less than 100");
		} 
		if(number==100){
			System.out.println("Number is equals to 100");
		}
	}

}
